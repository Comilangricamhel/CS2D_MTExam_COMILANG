let age = 28;  // Replace with the actual age

// Determine the age category and log the appropriate message
if (age < 5) {
    console.log("Toddler (under 5 years)");
} else if (age >= 5 && age <= 12) {
    console.log("Child (5–12 years)");
} else if (age >= 13 && age <= 19) {
    console.log("Teenager (13–19 years)");
} else if (age >= 18 && age <= 25) {
    console.log("Young Adult (18–25 years)");
} else if (age >= 40 && age <= 60) {
    console.log("Middle-Aged (40–60 years)");
} else {
    console.log("Senior (over 60 years)");
}
